class YOLOTrainer:


    def __init__(self, cfg: SystemConfig):
        self.cfg = cfg
        self.cfg.YOLO_OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    def _build_fold_yaml(
        self,
        fold_idx: int,   #The index of the current data fold
        train_txt_path: str, # text image path for train
        val_txt_path: str # for validate
    ) -> str:

        yaml_filename = f"fold_{fold_idx}_config.yaml"
        yaml_content = {
            'path':  str(self.cfg.BASE_DATA_DIR),
            'train': os.path.abspath(train_txt_path),
            'val':   os.path.abspath(val_txt_path),
            'nc':    self.cfg.NUM_DETECT_CLASSES,
            'names': self.cfg.CLASS_NAMES,
        }
        with open(yaml_filename, 'w') as yaml_file:
            yaml.dump(yaml_content, yaml_file)
        return os.path.abspath(yaml_filename)

    def train_single_fold(self, fold_idx: int, train_images: list, val_images: list) -> dict:

        # YOLO format
        train_txt = f"train_fold{fold_idx}.txt"
        val_txt   = f"val_fold{fold_idx}.txt"
        with open(train_txt, 'w') as f:
            f.write("\n".join(train_images))
        with open(val_txt, 'w') as f:
            f.write("\n".join(val_images))

        fold_yaml = self._build_fold_yaml(fold_idx, train_txt, val_txt)


        torch.cuda.empty_cache()
        detector = YOLO(self.cfg.YOLO_BASE_MODEL)

        detector.train(
            data       = fold_yaml,
            epochs     = self.cfg.YOLO_EPOCHS,
            imgsz      = self.cfg.YOLO_IMG_SIZE,
            batch      = self.cfg.YOLO_BATCH,
            device     = self.cfg.YOLO_DEVICE,
            seed       = self.cfg.RANDOM_SEED,
            workers    = self.cfg.YOLO_WORKERS,
            deterministic = True,


            hsv_h = self.cfg.YOLO_HSV_H,
            hsv_s = self.cfg.YOLO_HSV_S,
            hsv_v = self.cfg.YOLO_HSV_V,
            scale = self.cfg.YOLO_SCALE,

            mosaic       = self.cfg.YOLO_MOSAIC,
            mixup        = self.cfg.YOLO_MIXUP,
            copy_paste   = self.cfg.YOLO_COPY_PASTE,
            close_mosaic = self.cfg.YOLO_CLOSE_MOSAIC,
            erasing      = self.cfg.YOLO_ERASING,

            optimizer = self.cfg.YOLO_OPTIMIZER,
            lr0       = self.cfg.YOLO_LR,

            project = str(self.cfg.YOLO_OUTPUT_DIR),
            name    = f'fold_{fold_idx}',
        )

        # save result
        val_stats = detector.val()
        fold_results = val_stats.results_dict if val_stats else {}
        return fold_results
    # Executes K-Fold training.
    def run_kfold_training(self) -> dict:

        all_images = sorted([
            str(p) for p in self.cfg.TRAIN_IMG_DIR.iterdir()
            if p.suffix.lower() in ['.jpg', '.jpeg', '.png']
        ])

        splitter = KFold(
            n_splits   = self.cfg.CONVNEXT_N_FOLDS,
            shuffle    = True,
            random_state = self.cfg.RANDOM_SEED,
        )

        accumulated_results = {}

        for fold_num, (train_idx, val_idx) in enumerate(splitter.split(all_images)):
            if fold_num != self.cfg.YOLO_ACTIVE_FOLD:
                continue

            print(f"\n{'='*60}")
            print(f" YOLO11 Training - Fold {fold_num + 1}/{self.cfg.CONVNEXT_N_FOLDS}")
            print(f"{'='*60}")

            train_list = [all_images[i] for i in train_idx]
            val_list   = [all_images[i] for i in val_idx]

            fold_metrics = self.train_single_fold(fold_num, train_list, val_list)
            accumulated_results[fold_num] = fold_metrics

            # Save an instant copy of the results
            backup_path = self.cfg.YOLO_OUTPUT_DIR / 'kfold_metrics_backup.json'
            with open(backup_path, 'w') as bkp:
                json.dump(accumulated_results, bkp, indent=2)
            print(f" The result saved in {backup_path}")

        return accumulated_results

