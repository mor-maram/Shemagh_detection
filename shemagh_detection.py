def run_training_epoch(
    model: nn.Module,
    data_loader: DataLoader,
    optimizer: torch.optim.Optimizer,
    loss_fn: nn.Module,
    device: torch.device
) -> tuple:

    model.train()
    total_loss    = 0.0
    correct_count = 0
    sample_count  = 0

    for batch_images, batch_labels in tqdm(data_loader, desc="training", leave=False):
        batch_images = batch_images.to(device)
        batch_labels = batch_labels.to(device)

        optimizer.zero_grad()
        logits = model(batch_images)
        batch_loss = loss_fn(logits, batch_labels)
        batch_loss.backward()
        optimizer.step()

        total_loss    += batch_loss.item()
        preds          = torch.argmax(logits, dim=1)
        correct_count += (preds == batch_labels).sum().item()
        sample_count  += batch_labels.size(0)

    avg_loss = total_loss / len(data_loader)
    accuracy = correct_count / sample_count
    return avg_loss, accuracy


def run_validation_epoch(
    model: nn.Module,
    data_loader: DataLoader,
    loss_fn: nn.Module,
    device: torch.device
) -> tuple:

    model.eval()
    total_loss = 0.0
    all_preds  = []
    all_labels = []

    with torch.no_grad():
        for batch_images, batch_labels in tqdm(data_loader, desc="التحقق", leave=False):
            batch_images = batch_images.to(device)
            batch_labels = batch_labels.to(device)

            logits     = model(batch_images)
            batch_loss = loss_fn(logits, batch_labels)
            total_loss += batch_loss.item()

            preds = torch.argmax(logits, dim=1)
            all_preds.extend(preds.cpu().numpy())
            all_labels.extend(batch_labels.cpu().numpy())

    avg_loss    = total_loss / len(data_loader)
    val_acc     = accuracy_score(all_labels, all_preds)
    val_f1      = f1_score(all_labels, all_preds, zero_division=0)
    return avg_loss, val_acc, val_f1


class ConvNeXtClassifierTrainer:

    def __init__(self, cfg: SystemConfig):
        self.cfg = cfg
        self.cfg.CONVNEXT_OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    def _build_combined_dataframe(self) -> pd.DataFrame:

        df_original = pd.read_csv(self.cfg.TRAIN_CSV)
        df_original['path'] = df_original['filename'].apply(
            lambda fn: str(self.cfg.TRAIN_IMG_DIR / fn)
        )

        # Download fake labels (if any)
        combined_frames = [df_original]

        if self.cfg.PSEUDO_CSV_PATH.exists():
            df_pseudo = pd.read_csv(self.cfg.PSEUDO_CSV_PATH)
            #  label → right_place
            if 'label' in df_pseudo.columns:
                df_pseudo = df_pseudo.rename(columns={'label': 'right_place'})
            df_pseudo['path'] = df_pseudo['filename'].apply(
                lambda fn: str(self.cfg.TEST_IMG_DIR / fn)
            )
            combined_frames.append(df_pseudo)
            print(f"Added {len(df_pseudo)} pseudo-labels from: {self.cfg.PSEUDO_CSV_PATH}")

        else:
            print("Pseudo-labels file not found, training on original data only.")

        df_combined = pd.concat(combined_frames, axis=0, ignore_index=True)
        class_distribution = df_combined['right_place'].value_counts().to_dict()
        print(f"Class distribution after merging: {class_distribution}")
        return df_combined

    def _compute_class_weights(self, df: pd.DataFrame) -> torch.Tensor:
        counts     = df['right_place'].value_counts()
        n_negative = counts.get(0, 1)  # number of negative
        n_positive = counts.get(1, 1)  # Number of Positive
        # Positive class weight = ratio of negative samples to positive samples
        positive_weight = n_negative / n_positive
        weights = torch.tensor([1.0, float(positive_weight)]).to(self.cfg.DEVICE)
        print(f"Class weights: [1.0, {positive_weight:.3f}]")
        return weights

    def train_all_folds(self) -> None:
        df_full    = self._build_combined_dataframe()
        cls_weights = self._compute_class_weights(df_full)
        loss_fn    = nn.CrossEntropyLoss(weight=cls_weights)

        fold_splitter = StratifiedKFold(
            n_splits     = self.cfg.CONVNEXT_N_FOLDS,
            shuffle      = True,
            random_state = self.cfg.RANDOM_SEED,
        )

        for fold_num, (train_idx, val_idx) in enumerate(
            fold_splitter.split(df_full, df_full['right_place'])
        ):
            # Train the active fold only
            if fold_num > 0:
                break

            print(f"\n{'='*60}")
            print(f" ConvNeXt Training - Fold {fold_num + 1}/{self.cfg.CONVNEXT_N_FOLDS}")
            print(f"{'='*60}")

            fold_train_df = df_full.iloc[train_idx].reset_index(drop=True)
            fold_val_df   = df_full.iloc[val_idx].reset_index(drop=True)


            train_dataset = ShemaghClassificationDataset(
                fold_train_df,
                transform=get_classification_transforms('train', self.cfg)
            )
            val_dataset = ShemaghClassificationDataset(
                fold_val_df,
                transform=get_classification_transforms('val', self.cfg)
            )

            train_loader = DataLoader(
                train_dataset,
                batch_size  = self.cfg.CONVNEXT_BATCH,
                shuffle     = True,
                num_workers = 0,  # 0 Safe on Windows and most environments
            )
            val_loader = DataLoader(
                val_dataset,
                batch_size  = self.cfg.CONVNEXT_BATCH,
                shuffle     = False,
                num_workers = 0,
            )

            # Form creation: ConvNeXt designer built on ImageNet-22k
            convnext = timm.create_model(
                self.cfg.CONVNEXT_MODEL_NAME,
                pretrained   = True,
                num_classes  = self.cfg.CONVNEXT_NUM_CLASSES,
            ).to(self.cfg.DEVICE)

            optimizer = optim.AdamW(
                convnext.parameters(),
                lr           = self.cfg.CONVNEXT_LR,
                weight_decay = self.cfg.CONVNEXT_WEIGHT_DECAY,
            )

            best_f1    = 0.0
            model_path = self.cfg.CONVNEXT_OUTPUT_DIR / f'convnext_fold{fold_num}_best.pth'

            for epoch_num in range(self.cfg.CONVNEXT_EPOCHS):
                train_loss, train_acc = run_training_epoch(
                    convnext, train_loader, optimizer, loss_fn, self.cfg.DEVICE
                )
                val_loss, val_acc, val_f1 = run_validation_epoch(
                    convnext, val_loader, loss_fn, self.cfg.DEVICE
                )

                print(
                    f"  The era {epoch_num+1:02d}/{self.cfg.CONVNEXT_EPOCHS} | "
                    f"Training loss : {train_loss:.4f} | "
                    f"F1 Verification: {val_f1:.4f}"
                )

                # Save the model if F1 improves
                if val_f1 > best_f1:
                    best_f1 = val_f1
                    torch.save(convnext.state_dict(), model_path)
                    print(f" ✓ Saved the best model (F1={best_f1:.4f}) to: {model_path}")

            print(f"\n Fold {fold_num+1} finished. Best F1: {best_f1:.4f}")
            del convnext, optimizer
            torch.cuda.empty_cache()

    def get_best_model_path(self) -> Path:
        return (
            self.cfg.CONVNEXT_OUTPUT_DIR
            / f'convnext_fold{self.cfg.CONVNEXT_ACTIVE_FOLD}_best.pth'
        )

"""# Section 8: Final Inference Pipeline (TTA + WBF + NMS + ConvNeXt)
Implements the final multi-stage inference sequence for submission.
          It runs multi-scale YOLO predictions with Test-Time Augmentation (TTA),
          fuses bounding boxes using Weighted Boxes Fusion (WBF), applies
          class-wise NMS filtering, conditions the ConvNeXt wearing classifier
          on positive detections, and formats the output into standard submission strings.
"""

class FinalInferencePipeline:

    def __init__(
        self,
        yolo_path: str,
        convnext_path: str,
        cfg: SystemConfig
    ):
        self.cfg = cfg

        # Download YOLO Template
        print(f"Download YOLO from: {yolo_path}")
        self.yolo_detector = YOLO(yolo_path)

        # Download ConvNeXt Template
        print(f"Download ConvNeXt from: {convnext_path}")
        self.classifier = timm.create_model(
            cfg.CONVNEXT_MODEL_NAME,
            pretrained  = False,
            num_classes = cfg.CONVNEXT_NUM_CLASSES,
        )
        saved_weights = torch.load(convnext_path, map_location=cfg.DEVICE)
        self.classifier.load_state_dict(saved_weights)
        self.classifier.to(cfg.DEVICE)
        self.classifier.eval()
        self.inference_transform = get_classification_transforms('val', cfg)

    def _detect_with_tta(self, image_path: str) -> tuple:
        boxes_tta  = []
        scores_tta = []
        labels_tta = []

        for scale in self.cfg.FINAL_TTA_SCALES:
            scale_results = self.yolo_detector.predict(
                image_path,
                imgsz   = scale,
                conf    = self.cfg.FINAL_CONF_THRES,
                augment = True,
                verbose = False,
            )[0]

            if len(scale_results.boxes) > 0:
                boxes_tta.append(scale_results.boxes.xyxyn.cpu().tolist())
                scores_tta.append(scale_results.boxes.conf.cpu().tolist())
                labels_tta.append(scale_results.boxes.cls.cpu().tolist())

        if not boxes_tta:
            return [], [], []

        # Apply WBF: Consolidates redundant boxes by averaging spatial coordinates.
        # Superior to standard NMS as it retains variance through fusion rather than suppression.
        fused_boxes, fused_scores, fused_labels = weighted_boxes_fusion(
            boxes_tta,
            scores_tta,
            labels_tta,
            weights  = None,
            iou_thr  = self.cfg.FINAL_IOU_WBF,
            skip_box_thr = self.cfg.FINAL_CONF_THRES,
        )
        return fused_boxes, fused_scores, fused_labels

    def _apply_classwise_nms(
        self,
        raw_boxes: list,
        raw_scores: list,
        raw_labels: list
    ) -> tuple:
        # Apply class-wise NMS post-WBF to prevent redundant overlapping boxes per object.
        if len(raw_boxes) == 0:
            return [], [], []

        boxes_t  = torch.tensor(raw_boxes,  dtype=torch.float32)
        scores_t = torch.tensor(raw_scores, dtype=torch.float32)
        labels_t = torch.tensor(raw_labels, dtype=torch.int64)

        kept_indices = []
        for cls_id in labels_t.unique():
            class_mask     = (labels_t == cls_id)
            class_boxes    = boxes_t[class_mask]
            class_scores   = scores_t[class_mask]
            original_idx   = torch.where(class_mask)[0]

            # Standard NMS per category
            nms_keep = tv_ops.nms(class_boxes, class_scores, self.cfg.FINAL_NMS_THRES)
            kept_indices.extend(original_idx[nms_keep].tolist())

        kept_indices = sorted(kept_indices)
        return (
            [raw_boxes[i]  for i in kept_indices],
            [raw_scores[i] for i in kept_indices],
            [raw_labels[i] for i in kept_indices],
        )

    def _classify_placement(self, image_path: str) -> int:

        image_rgb    = load_image_rgb(image_path)
        pil_image    = Image.fromarray(image_rgb)
        input_tensor = self.inference_transform(pil_image).unsqueeze(0).to(self.cfg.DEVICE)

        with torch.no_grad():
            logits       = self.classifier(input_tensor)
            probabilities = torch.softmax(logits, dim=1)
            predicted_cls = torch.argmax(probabilities, dim=1).item()

        return predicted_cls

    def process_single_image(self, image_path: str) -> dict:

        filename = os.path.basename(image_path)

        # Stage 1: YOLO TTA
        raw_boxes, raw_scores, raw_labels = self._detect_with_tta(image_path)

        # Stage 2: NMS For each category
        nms_boxes, nms_scores, nms_labels = self._apply_classwise_nms(
            raw_boxes, raw_scores, raw_labels
        )

        # Determine whether there is a face covering or a headscarf
        has_face    = any(int(l) == 0 for l in nms_labels)
        has_shemagh = any(int(l) == 1 for l in nms_labels)

        # Stage 3: ConvNeXt (only if something is revealed)
        is_correct_placement = 0
        if has_face or has_shemagh:
            is_correct_placement = self._classify_placement(image_path)

        # Build prediction series
        parts = [
            f"{int(l)} {s:.4f} "
            f"{convert_xyxy_to_xywh(b)[0]:.4f} "
            f"{convert_xyxy_to_xywh(b)[1]:.4f} "
            f"{convert_xyxy_to_xywh(b)[2]:.4f} "
            f"{convert_xyxy_to_xywh(b)[3]:.4f}"
            for b, s, l in zip(nms_boxes, nms_scores, nms_labels)
        ]
        prediction_str = " ".join(parts) if parts else "-"

        return {
            'filename':          filename,
            'right_place':       is_correct_placement,
            'prediction_string': prediction_str,
        }

    def generate_submission(self) -> pd.DataFrame:
        test_images = sorted([
            str(p) for p in self.cfg.TEST_IMG_DIR.iterdir()
            if p.suffix.lower() in ['.jpg', '.jpeg', '.png']
        ])

        print(f"\nStarting final inference on {len(test_images)} images...")
        submission_rows = []

        for img_path in tqdm(test_images, desc="Final Conclusion"):
            row = self.process_single_image(img_path)
            submission_rows.append(row)

        df_submission = pd.DataFrame(submission_rows)
        df_submission.to_csv(self.cfg.SUBMISSION_PATH, index=False)

        positive_count = df_submission['right_place'].sum()
        print(f"\n✓ Saved submission file to: {self.cfg.SUBMISSION_PATH}")
        print(f" Total positive predictions (correct placement): {positive_count}")
        print(f" Total images: {len(df_submission)}")

        return df_submission

